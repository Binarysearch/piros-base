package com.example.controllers;

public class ExampleRequestDto {

    private String name;
    private Integer age;

    public ExampleRequestDto() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }


}